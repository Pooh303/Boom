export function useTranslation() {
  const context = useContext(PageContext);
  const language = context?.language || "th";

  const t = (key) => {
    const langDict = dict[language] || dict["th"];
    return langDict[key] || dict["en"][key] || key;
  };

  return { t, language };
}
